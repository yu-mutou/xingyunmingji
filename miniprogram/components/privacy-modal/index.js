Component({
  properties: {
    visible: { type: Boolean, value: false },
  },

  data: {
    showFullPolicy: false,
    fullPolicyTitle: '',
    fullPolicyContent: '',
  },

  methods: {
    onAgree() {
      // Store consent flag
      try {
        wx.setStorageSync('__privacy_agreed__', true);
      } catch (e) { /* ignore */ }
      this.triggerEvent('onAgree');
    },

    onDisagree() {
      wx.showModal({
        title: '温馨提示',
        content: '如不同意隐私政策，将无法使用小程序的完整功能。您确定要退出吗？',
        confirmText: '我再看看',
        cancelText: '确定退出',
        success: (res) => {
          if (!res.confirm) {
            // User chose to exit
            wx.exitMiniProgram();
          }
        },
      });
    },

    onViewFullPrivacy() {
      this.setData({
        showFullPolicy: true,
        fullPolicyTitle: '隐私政策',
        fullPolicyContent: FULL_PRIVACY,
      });
    },

    onViewFullTerms() {
      this.setData({
        showFullPolicy: true,
        fullPolicyTitle: '用户服务协议',
        fullPolicyContent: FULL_TERMS,
      });
    },

    onCloseFullPolicy() {
      this.setData({ showFullPolicy: false });
    },
  },
});
